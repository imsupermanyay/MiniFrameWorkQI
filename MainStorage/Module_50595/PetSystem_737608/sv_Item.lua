local PetSkillBook = { 
}
--生成技能书
local function GenrateSkillBook()
    for _, value in pairs(script.Parent.PetSkill.Children) do
        local skill = require(value)
        local classname = skill.ClassName
        local obj  = {}
        obj.Name = skill.Name
        obj.Quality = skill.Quality
        obj.Icon = skill.Icon
        obj.Size = skill.Size 
        obj.Introduce = skill.Introduce
        obj.Data = skill.Data
 

        PetSkillBook[classname] = obj
    end
end
 
GenrateSkillBook()





--穿着宠物装备的函数 
function PetEquipmentFunc(itemdata,ply,bagobj)
    print("玩家给宠物使用了装备") 

    --获得Pet管理
    local PlayerPet = data.GetData(ply.UserId,"PetManager")
    local result = PlayerPet:Equipment(bagobj.ChoisePet,itemdata,ply)

    return result 
end

 
--技能书函数
function PetSkillBookFunc(itemdata,ply,bagobj)
    print("玩家给宠物使用了技能书") 
 
    --获得Pet管理
    local PlayerPet = data.GetData(ply.UserId,"PetManager")
    local result = PlayerPet:AddSkill(bagobj.ChoisePet,itemdata)

    return result 
end



 
--arg 1 Item表  arg2 类别 arg3 类名 arg4 使用函数 
Item.Func.LoadItem(PetEquipment,"PetEquipment","宠物装备",PetEquipmentFunc)
Item.Func.LoadItem(PetSkillBook,"PetSkillBook","宠物技能书",PetSkillBookFunc)